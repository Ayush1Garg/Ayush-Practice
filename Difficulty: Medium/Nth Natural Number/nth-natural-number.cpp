class Solution {
  public:
    long long findNth(long long n) {
        // code here.
        long long ans = 0, mul = 1;
        while(n){
            ans = (n%9)*mul + ans;
            mul = mul*10;
            n = n/9;
        }
        return ans;
    }
};

//{ Driver Code Starts.
int main() {
    int t;
    cin >> t;
    while (t--) {
        long long n, ans;
        cin >> n;
        Solution obj;
        ans = obj.findNth(n);
        cout << ans << endl;
    }
}

